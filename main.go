func main() {
	flag.Parse()
	c := &serial.Config{Name: flag.Arg(0), Baud: 115200, ReadTimeout: 2 * time.Second}
	s, err := serial.OpenPort(c)
	if err != nil {
		log.Fatal(err)
	}
	cobsserial.S = s
	w := rain.NewCloud()
	i := 0
	for {
		i++
		m := w.Generate(i)
		err = cobsserial.Write(m)
		if err != nil {
			log.Fatal(err)
		}
		time.Sleep(time.Second / 60)
	}
}
